int do_task_master_core(struct sof *sof)
{
	int ret;

/* TODO: The tplg and tplg_size set is temporary code to test the feature. It
 * will be later set by the boot loader.
 */
#if defined(CONFIG_TOPOLOGY)
	uint8_t *tplg = test_tone_playback_tplg;
	size_t tplg_size = test_tone_playback_tplg_len;
#else
	uint8_t *tplg = NULL;
	size_t tplg_size = 0;
#endif

#ifdef STATIC_PIPE
	struct audio_data pdata;
#endif
	/* init default audio components */
	sys_comp_init();
	sys_comp_dai_init();
	sys_comp_host_init();
	sys_comp_mixer_init();
	sys_comp_mux_init();
	sys_comp_switch_init();
	sys_comp_volume_init();
	sys_comp_src_init();
	sys_comp_tone_init();
	sys_comp_eq_iir_init();
	sys_comp_eq_fir_init();

#if STATIC_PIPE
	/* init static pipeline */
	pdata.p = init_static_pipeline();
	if (pdata.p == NULL)
		panic(SOF_IPC_PANIC_TASK);
#endif

	/* let host know DSP boot is complete */
	platform_boot_complete(0);

	/* Setup pipelines with topology data if the pointer is set. */
	if (tplg != NULL) {
		ret = tplg_parse(sof->ipc, tplg, tplg_size);
		if (ret)
			trace_error(TRACE_CLASS_PIPE, "etp");
	}

	/* main audio IPC processing loop */
	while (1) {
		/* sleep until next IPC or DMA */
		sa_enter_idle(sof);
		wait_for_interrupt(0);

		/* now process any IPC messages from host */
		ipc_process_msg_queue();

		/* schedule any idle tasks */
		schedule();
	}

	/* something bad happened */
	return -EIO;
}

int do_task_slave_core(struct sof *sof)
{
	/* main audio IDC processing loop */
	while (1) {
		/* sleep until next IDC */
		wait_for_interrupt(0);

		/* schedule any idle tasks */
		schedule();
	}

	/* something bad happened */
	return -EIO;
}
